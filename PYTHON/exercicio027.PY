#Crie um programa que vai pegar os valores em uma lista utilizando for e dizer se cada um desses elementos é maior que 7.

numeros = [2, 3, 10 , 7, 8 , 9]
for x in range(6):
    numero = numeros [x]
    
    if numero > 7 :
        print(f"{numero} é maior que 7!")

    else:
        print(f"{numero} Não é maior que 7!")        


