import math
angulo = float(input("Digita o 창ngulo que voce quer: "))

seno = math.sin(math.radians(angulo))
print(f'O 창ngulo de {angulo} tem o seno de {seno:.2f}')

cosseno = math.cos(math.radians(angulo))
print(f'O 창ngulo de {angulo} tem o seno de {cosseno:.2f}')
 
tangeno = math.tan(math.radians(angulo))
print(f'O 창ngulo de {angulo} tem o seno de {tangeno:.2f}')


