a = input("Digita algo: ")

print('O tipo primitivo deste valor é ',type(a))
print('So tem espaços?',a.isspace())
print('È um número?',a.isnumeric())
print('È alfabeto?',a.isalpha())
print('È um alfa-número?',a.isalnum())
print('Esta em maiucusla?',a.isupper())
print('Esta em minuscula?',a.islower())
print('Esta Capitalizada?',a.istitle())







